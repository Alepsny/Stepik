a, b = int(input()), int(input())
div1, dig1, div2 = 1, a, 1
for i in range(a, b + 1):
    for j in range(1, i + 1):
        if i % j == 0:
            div2 += j
            dig2 = i
    if div2 >= div1:
        div1 = div2
        dig1 = dig2
    div2 = 0
print(dig1, div1)            
