def cesar(s, n):
    new = ''
    for i in s:
        if 97 <= ord(i) <= 122:
            if ord(i) + n > 122:
                new += chr(97 + (ord(i) + n) % 122 - 1)
            else:
                new += chr(ord(i) + n)
        elif 65 <= ord(i) <= 90:
            if ord(i) + n > 90:
                new += chr(65 + (ord(i) + n) % 90 - 1)
            else:
                new += chr(ord(i) + n)
        else:
            new += i
    return new

s = input()
new, temp = '', ''
for i in s:
    if i.isalpha():
        temp += i
    elif temp != '':
        new += cesar(temp, len(temp))
        new += i
        temp = ''
    else:
        new += i
print(new + cesar(temp, len(temp)))
