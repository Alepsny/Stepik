# –æ–±—ä—è–≤–ª–µ–Ω–∏–µ —Ñ—É–Ω–∫—Ü–∏–∏
def is_correct_bracket(text):
    count = 0
    for i in range(len(text)):
        if count < 0:
            return False
        if text[i] == "(":
            count += 1
        elif text[i] == ")":
            count -= 1
    return count == 0

# —Å—á–∏—Ç—ã–≤–∞–µ–º –¥–∞–Ω–Ω—ã–µ
