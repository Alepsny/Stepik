n = int(input())
count3, countld, countch, s, p, count05 = 0, 0, 0, 0, 1, 0
lastd = n % 10
while n > 0:
    lg = n % 10
    if lg == 3:
        count3 += 1
    if lg == lastd:
        countld += 1
    if lg % 2 == 0:
        countch += 1
    if lg > 5:
        s += lg
    if lg > 7:
        p *= lg
    if lg == 0 or lg == 5:
        count05 += 1
    n //= 10
print(count3, countld, countch, s, p, count05, sep="\n")
