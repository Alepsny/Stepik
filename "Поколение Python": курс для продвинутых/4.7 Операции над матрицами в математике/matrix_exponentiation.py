def mat_def(n, mat, mat_):
    mat_new = [[0 for j in range(n)] for i in range(n)]
    for i in range(n):
        for j in range(n):
            for k in range(n):
                mat_new[i][j] += mat[i][k] * mat_[k][j]
    return mat_new


n = int(input())
mat = [[int(j) for j in input().split()] for i in range(n)]
mat_ = mat
m = int(input())

for i in range(m - 1):
    mat = mat_def(n, mat, mat_)
for i in range(n):
    print(*mat[i])
