n, flag = int(input()), True
mat = [[int(i) for i in input().split()] for j in range(n)]
for i in range(n):
    for j in range(n):
        if mat[i][j] != mat[n - j - 1][n - i - 1]:
            flag = False
            break
if flag:
    print('YES')
else:
    print('NO')

