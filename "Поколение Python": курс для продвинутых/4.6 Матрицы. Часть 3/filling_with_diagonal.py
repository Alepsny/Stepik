n, m = [int(i) for i in input().split()]
mat, count = [[0 for j in range(m)] for i in range(n)], 1
for i in range(n + m):
    for j in range(min(i, m -1), -1, -1):
        if i - j < n:
            mat[i-j][j] = count
            count += 1
for i in range(n):
    print(*mat[i])


