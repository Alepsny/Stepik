x, y, dx, dy = 0, 0, 0, 1
n, m = [int(i) for i in input().split()]
arr = [[0 for j in range(m)] for i in range(n)]
for i in range(1, n * m + 1):
    arr[x][y] = i
    if 0 <= x + dx < n and 0 <= y + dy < m and arr[x + dx][y + dy] == 0:
        x, y = x + dx, y + dy
    else:
        dx, dy = dy, -dx
        x, y = x + dx, y + dy

for i in range(n):
    print(*arr[i])

